Chris Ahlfeldt has over a decade of in-depth work experience in the clean energy and sustainability industries working primarily in the North American, Asian, and African markets. Since founding Blue Horizon ECS he has independently and collaboratively sold, managed and implemented various projects ranging from low-income energy services deployment to national renewable energy strategies.

